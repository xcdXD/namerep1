def f(x):
    res = []
    while x > 0:
        res.append(x % 27)      #в скобках пишем основание
        x //= 27                #после //= тоже основание
    return res[::-1]

cnt = 0
a = 2 * 2187 ** 567 + 729 ** 566 - 2 * 243 ** 565 + 81 ** 564 - 2 * 27 ** 563 - 6561
b = f(a)
for x in b:
    if x % 2 == 0 and x > 9:
        cnt += 1
        print(cnt)
